    #[test]
    fn test_float_encoder_simple() {
        let mut s = FloatEncoder::new();

        s.write(12_f64);
        s.write(12_f64);
        s.write(24_f64);

        // extra tests

        // floating point masking/shifting bug
        s.write(13_f64);
        s.write(24_f64);

        // delta-of-delta sizes
        s.write(24_f64);
        s.write(24_f64);
        s.write(24_f64);

        s.flush();

        let b = s.bytes().unwrap();

        let mut it = FloatDecoder::new(b.as_slice()).unwrap();

        let want = [
            12_f64, 12_f64, 24_f64, 13_f64, 24_f64, 24_f64, 24_f64, 24_f64,
        ];

        for w in want {
            assert_eq!(it.next(), true, "Next()=false for {}, want true", w);

            let vv = it.values();
            assert_eq!(w, vv, "Values()=({}), want ({})\n", vv, w);
        }

        assert_eq!(it.next(), false, "Next()=true .., want false");
        assert_eq!(it.error().is_none(), true, "it.Error()=%v, want nil");
    }

